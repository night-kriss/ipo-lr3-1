a=int(input("Введите целое число:")) # Запрашиваем у пользователя целое число
 # Проверяем, является ли число чётным или нечётным
if a % 2 == 0:          #если четное 
    print(f"Число {a} является чётным.")  #то выводим это
else:     #если нет
    print(f"Число {a} является нечётным.") #то выводим это